public class areaOfACircle {
    public areaOfACircle() {
    }
 
    public static void main(String[] args) {
        int rad = 10;
        double pi = 3.14;
        double area = pi * rad * rad ;
        System.out.println("The radius of the circle is " + rad + " and the area is " + area);
    }
 }